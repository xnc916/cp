package com.xiongyuan.lottery.mypage.bean;

/**
 * Created by gameben on 2017-06-07.
 */

public class DrawMoneyInfo {
    private String dailyCashTimes;
    private String dailyCashFree;
    private String cashCommission;
    private String maxCash;
    private String minCash;
    private String cashStartTime;
    private String cashEndTime;
    private String cashConsShare;

    @Override
    public String toString() {
        return "DrawMoneyInfo{" +
                "dailyCashTimes='" + dailyCashTimes + '\'' +
                ", dailyCashFree='" + dailyCashFree + '\'' +
                ", cashCommission='" + cashCommission + '\'' +
                ", maxCash='" + maxCash + '\'' +
                ", minCash='" + minCash + '\'' +
                ", cashStartTime='" + cashStartTime + '\'' +
                ", cashEndTime='" + cashEndTime + '\'' +
                ", cashConsShare='" + cashConsShare + '\'' +
                '}';
    }

    public DrawMoneyInfo(String dailyCashTimes, String dailyCashFree, String cashCommission, String maxCash, String minCash, String cashStartTime, String cashEndTime, String cashConsShare) {
        this.dailyCashTimes = dailyCashTimes;
        this.dailyCashFree = dailyCashFree;
        this.cashCommission = cashCommission;
        this.maxCash = maxCash;
        this.minCash = minCash;
        this.cashStartTime = cashStartTime;
        this.cashEndTime = cashEndTime;
        this.cashConsShare = cashConsShare;
    }

    public String getDailyCashTimes() {
        return dailyCashTimes;
    }

    public void setDailyCashTimes(String dailyCashTimes) {
        this.dailyCashTimes = dailyCashTimes;
    }

    public String getDailyCashFree() {
        return dailyCashFree;
    }

    public void setDailyCashFree(String dailyCashFree) {
        this.dailyCashFree = dailyCashFree;
    }

    public String getCashCommission() {
        return cashCommission;
    }

    public void setCashCommission(String cashCommission) {
        this.cashCommission = cashCommission;
    }

    public String getMaxCash() {
        return maxCash;
    }

    public void setMaxCash(String maxCash) {
        this.maxCash = maxCash;
    }

    public String getMinCash() {
        return minCash;
    }

    public void setMinCash(String minCash) {
        this.minCash = minCash;
    }

    public String getCashStartTime() {
        return cashStartTime;
    }

    public void setCashStartTime(String cashStartTime) {
        this.cashStartTime = cashStartTime;
    }

    public String getCashEndTime() {
        return cashEndTime;
    }

    public void setCashEndTime(String cashEndTime) {
        this.cashEndTime = cashEndTime;
    }

    public String getCashConsShare() {
        return cashConsShare;
    }

    public void setCashConsShare(String cashConsShare) {
        this.cashConsShare = cashConsShare;
    }
}
