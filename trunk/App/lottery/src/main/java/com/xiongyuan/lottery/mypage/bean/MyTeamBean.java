public class MyTeamBean {
    private String id;
    private String avatar;
    private String username;
    private String usableAmount;
    private String frozenAmount;
    private String type;
    private String date;
    private String maxRebate;
    private String minRebate;
    private String onlineTime;

    public MyTeamBean(String id,String username, String usableAmount,String frozenAmount, String type, String date,String maxRebate, String minRebate, String onlineTime) {
        this.id=id;
        this.username = username;
        this.usableAmount = usableAmount;
        this.frozenAmount=frozenAmount;
        this.type = type;
        this.date=date;
        this.maxRebate = maxRebate;
        this.minRebate = minRebate;
        this.onlineTime = onlineTime;
    }

    public String getFrozenAmount() {
        return frozenAmount;
    }

    public void setFrozenAmount(String frozenAmount) {
        this.frozenAmount = frozenAmount;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getUsableAmount() {
        return usableAmount;
    }

    public void setUsableAmount(String usableAmount) {
        this.usableAmount = usableAmount;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getMaxRebate() {
        return maxRebate;
    }

    public void setMaxRebate(String maxRebate) {
        this.maxRebate = maxRebate;
    }

    public String getMinRebate() {
        return minRebate;
    }

    public void setMinRebate(String minRebate) {
        this.minRebate = minRebate;
    }

    public String getOnlineTime() {
        return onlineTime;
    }

    public void setOnlineTime(String onlineTime) {
        this.onlineTime = onlineTime;
    }
}
