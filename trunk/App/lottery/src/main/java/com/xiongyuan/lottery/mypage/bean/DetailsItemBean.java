package com.xiongyuan.lottery.mypage.bean;

import java.io.Serializable;

/**
 * Created by Administrator on 2017-05-30.
 */

public class DetailsItemBean implements Serializable {
    private String accountName;
    private String cardNum;
    private String bankAddress;
    private String bankName;
    private String id;

    public DetailsItemBean(String id,String accountName, String cardNum, String bankAddress, String bankName) {
        this.id=id;
        this.accountName = accountName;
        this.cardNum = cardNum;
        this.bankAddress = bankAddress;
        this.bankName = bankName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAccountName() {
        return accountName;
    }

    public void setAccountName(String accountName) {
        this.accountName = accountName;
    }

    public String getCardNum() {
        return cardNum;
    }

    public void setCardNum(String cardNum) {
        this.cardNum = cardNum;
    }

    public String getBankAddress() {
        return bankAddress;
    }

    public void setBankAddress(String bankAddress) {
        this.bankAddress = bankAddress;
    }

    public String getBankName() {
        return bankName;
    }

    public void setBankName(String bankName) {
        this.bankName = bankName;
    }
}
