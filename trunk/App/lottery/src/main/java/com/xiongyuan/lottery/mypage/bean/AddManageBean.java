public class AddManageBean {
    private String expireTime;
    private String maxRebate;
    private String minRebate;
    private String regUrl;

    public AddManageBean(String expireTime, String maxRebate, String minRebate, String regUrl) {
        this.expireTime = expireTime;
        this.maxRebate = maxRebate;
        this.minRebate = minRebate;
        this.regUrl = regUrl;
    }

    public String getExpireTime() {
        return expireTime;
    }

    public void setExpireTime(String expireTime) {
        this.expireTime = expireTime;
    }

    public String getMaxRebate() {
        return maxRebate;
    }

    public void setMaxRebate(String maxRebate) {
        this.maxRebate = maxRebate;
    }

    public String getMinRebate() {
        return minRebate;
    }

    public void setMinRebate(String minRebate) {
        this.minRebate = minRebate;
    }

    public String getRegUrl() {
        return regUrl;
    }

    public void setRegUrl(String regUrl) {
        this.regUrl = regUrl;
    }
}
